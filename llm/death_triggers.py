def check_death_trigger(npc_name: str, user_input: str) -> bool:
    """
    유저 발화가 사망 트리거에 해당하는지 판단한다.

    Parameters
    ----------
    npc_name   : 현재 대화 중인 NPC 식별자
    user_input : 유저가 입력한 문장

    Returns
    -------
    True  → 즉각 사망 처리
    False → 정상 진행
    """
    # 1) 전체 공통 트리거 (치키 포함)
    for keyword in DEATH_TRIGGERS.get("__ALL__", []):
        if keyword in user_input:
            return True

    # 2) 치키 제외 공통 트리거 (루프 구조 발설)
    if npc_name != NPC_EXECUTOR:
        for keyword in DEATH_TRIGGERS.get("__NO_CHIKI__", []):
            if keyword in user_input:
                return True

    # 3) NPC 개별 트리거
    for keyword in DEATH_TRIGGERS.get(npc_name, []):
        if keyword in user_input:
            return True

    return False


def check_chiki_loop_reset(user_input: str) -> bool:
    """
    치키 대화 중 루프 강제 리셋 트리거 여부를 판단한다.
    금고 발설·연쇄살인·범인 자백은 치키에게도 루프 리셋으로 반응.
    (사망 아님 — chat_node.py에서 별도 분기 처리)
    """
    for keyword in DEATH_TRIGGERS.get("__ALL__", []):
        if keyword in user_input:
            return True
    return False