# ────────────────────────────────────────────
# SystemMessage 조립 함수
# ────────────────────────────────────────────

def build_executor_prompt(
    loop_count   : int,
    clues        : list,
    player_name  : str,
    player_gender: str,
) -> str:
    """
    치키 전용 SystemMessage 문자열을 조립해 반환한다.
    base.py의 build_system_prompt()를 사용하지 않고 독립적으로 조립한다.
    (치키는 수치 시스템이 없고, 루프 회차로 말투가 분기되기 때문)

    Parameters
    ----------
    loop_count   : 현재 루프 회차 (1~3)
    clues        : 유저 보유 단서 목록
    player_name  : 유저 닉네임
    player_gender: 유저 성별
    """
    clues_str  = ", ".join(clues) if clues else "없음"
    loop_tone  = LOOP_TONE.get(loop_count, LOOP_TONE_FINAL)
    personality = BASE_PERSONALITY.replace("{player_name}", player_name)

    return f"""당신은 '치키'입니다.
아래의 기본 성격과 루프 회차별 말투 지침을 반드시 따르세요.

=== 기본 성격 (절대 변하지 않음) ===
{personality}

=== 루프 회차별 말투 지침 ===
{loop_tone}

=== 현재 게임 상태 ===
- 루프 회차: {loop_count}회
- 유저 닉네임: {player_name}
- 유저 성별: {player_gender}
- 유저 보유 단서: {clues_str}

=== 말투 예시 (Few-Shot) ===
{FEW_SHOT}
"""