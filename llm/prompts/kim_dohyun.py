# ────────────────────────────────────────────
# SystemMessage 조립 함수
# ────────────────────────────────────────────

def build_kim_prompt(
    stats        : dict,
    loop_count   : int,
    clues        : list,
    player_name  : str,
    player_gender: str,
) -> str:
    """
    김도현 전용 SystemMessage 문자열을 조립해 반환한다.
    base.py의 build_system_prompt()를 사용해 수치를 반영한다.

    Parameters
    ----------
    stats        : 확정된 김도현 수치 딕셔너리 (config.py 참고)
    loop_count   : 현재 루프 회차
    clues        : 유저 보유 단서 목록
    player_name  : 유저 닉네임
    player_gender: 유저 성별
    """
    return build_system_prompt(
        npc_name        = "김도현",
        base_personality= BASE_PERSONALITY,
        stats           = stats,
        few_shot        = FEW_SHOT,
        loop_count      = loop_count,
        clues           = clues,
        player_name     = player_name,
        player_gender   = player_gender,
    )